def evaluate_context_quality(llm, context_chunks: List[str]) -> Dict:
    """
    LLM-as-a-Judge for context relevance, completeness, noise.
    """
    context = "\n".join(context_chunks)

    prompt = f"""
You are evaluating retrieved context for quiz generation.

Rate the following from 1 (poor) to 5 (excellent):

1. Relevance to topic
2. Completeness of information
3. Noise level (5 = very clean, no irrelevant info)

Context:
{context}

Return JSON only with keys:
relevance, completeness, noise
"""

    response = llm(prompt)[0]["generated_text"]
    return response


