def is_question_valid(llm, context, question_text):
    """
    Returns 1 if question is valid, else 0
    """
    prompt = f"""
Check if the following MCQ is VALID.

Rules:
- Must be answerable ONLY from context
- Must NOT use outside knowledge
- Must have 4 options (A-D)
- Must include one correct answer

Context:
{context}

Question:
{question_text}

Reply ONLY with:
VALID or INVALID
"""

    output = llm(prompt)
    verdict = output[0]["generated_text"].strip().upper()

    if "VALID" in verdict:
        return 1
    return 0

def generation_precision(llm, context_chunks, quiz_text):
    """
    Calculates precision for generated questions
    """
    context = " ".join(context_chunks[:2])  # keep context short

    # Split questions roughly
    questions = quiz_text.split("\n\n")

    valid_count = 0

    for q in questions:
        if len(q.strip()) < 50:
            continue

        valid = is_question_valid(llm, context, q)
        valid_count += valid

    total = len(questions)

    if total == 0:
        return 0.0

    return round(valid_count / total, 2)

