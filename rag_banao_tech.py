# --------------------------------

def concept_coverage_score(
    retrieved_chunks: List[str],
    key_concepts: List[str]
) -> float:
    """
    Measures how many important concepts appear
    in retrieved chunks.
    """
    found = set()
    for concept in key_concepts:
        for chunk in retrieved_chunks:
            if concept.lower() in chunk.lower():
                found.add(concept.lower())
                break
    return len(found) / len(key_concepts) if key_concepts else 0.0


def retrieval_precision(
    retrieved_chunks: List[str],
    topic_keywords: List[str]
) -> float:
    """
    Measures how many retrieved chunks are relevant.
    """
    relevant = 0
    for chunk in retrieved_chunks:
        if any(kw.lower() in chunk.lower() for kw in topic_keywords):
            relevant += 1
    return relevant / len(retrieved_chunks) if retrieved_chunks else 0.0


